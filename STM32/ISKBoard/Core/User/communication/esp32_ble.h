/**
  ******************************************************************************
  * @file    esp32_ble.h
  * @brief   ESP32 BLE 透传驱动（通过 AT 指令控制）
  *
  * 依赖 USART2（PA2=TX, PA3=RX），波特率 115200。
  *
  * 使用方式：
  *   ESP32_Init();           // 初始化 ESP32（检测 AT 响应）
  *   ESP32_Send(buf, len);   // 发送数据帧到 BLE
  ******************************************************************************
  */
#ifndef __ESP32_BLE_H
#define __ESP32_BLE_H

#include <stdint.h>

/* ================================================================
 *  API
 * ================================================================ */

/**
  * @brief  初始化 ESP32
  *         发送 AT 测试命令，等待 "OK" 响应。
  * @retval 0 = 成功, 1 = 超时/失败
  */
uint8_t ESP32_Init(void);

/**
  * @brief  通过 USART2 发送数据给 ESP32（透传/数据发送）
  * @param  buf   数据缓冲区
  * @param  len   数据长度
  */
void ESP32_Send(const uint8_t *buf, uint16_t len);

#endif /* __ESP32_BLE_H */
