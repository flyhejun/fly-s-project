#include "util.h"
#include <stdio.h>

/* ================================================================
 *  isqrt — 整数平方根
 *  二分查找，输入 32 位无符号整数，返回 16 位平方根。
 *  用于将 accel_sq 转换为 g 值，避免 float printf。
 * ================================================================ */
uint32_t isqrt(uint32_t n)
{
    if (n == 0) return 0;
    uint32_t lo = 1, hi = 65535;
    while (lo <= hi) {
        uint32_t mid = (lo + hi) / 2;
        if (mid * mid <= n) lo = mid + 1;
        else                 hi = mid - 1;
    }
    return hi;
}

/* ================================================================
 *  dump_hex — 以 HEX 格式打印缓冲区内容
 *  用于调试验证二进制帧打包是否正确。
 * ================================================================ */
void dump_hex(const uint8_t *buf, uint16_t len)
{
    uint16_t i;
    printf("[HEX] ");
    for (i = 0; i < len; i++) {
        printf("%02X ", buf[i]);
    }
    printf("\n");
}
